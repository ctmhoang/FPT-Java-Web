package entity;

/**
 *
 * @author Camer
 */
public class Info {

    private int id;
    private String name;
    private int age;
    private String img;
    private String description;

    public Info(int id, String name, int age, String img, String description) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.img = img;
        this.description = description;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getImg() {
        return "img/" + img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

}
